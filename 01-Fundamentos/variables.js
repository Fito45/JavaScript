/*
Variables
son contenedores que almancenan datos.

las variables se declaran usando las palabras recervadas

Var -> forma tradicional pero ya no se recomienda
Let -> forma moderna y recomendada
Const -> forma moderna pero el dato almacenado no se puede cambiar

Para declarar una variable usamos una palabra recervada
luego el nombre de la variable seguido de un signo "="
y un ";".

*/

var nombre= "Fito";

console.log(nombre)

let edad= 20;

console.log(edad)

const vivo= true;

console.log(vivo)

/*
las variables declaradas con var y let pueden cambiar a lo largo
del codigo, siempre y cuando su alcance lo permita.

Y las variables declaradas con Const no pueden cambiar.

Para cambiar el valor de una variable:

[nombre variable]= "nuevo valor";

*/

nombre = "Hamburger";

console.log(nombre)

edad = 29;

console.log(edad)
